#include <iostream>

using namespace std;

int main()
{
    double v, t, s;

    cout << "Masukkan kecepatan km/jam (v): ";
    cin >> v;

    cout << "Masukkan waktu/jam (t): ";
    cin >> t;

    s = v * t;

    cout << "jarak (s) yang ditempuh adalah: " << s << " km" << endl;

    return 0;
}

